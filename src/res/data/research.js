const schemaCheck = () => {
  // We use the json-schema.org spec.
  // See https://json-schema.org/specification.html
  const schema = {
    description: 'An array of research data.',
    type: 'array',
    items: {
      description: 'Research work.',
      type: 'object',
      required: [
        'title',
        'authors',
        'venue',
        'venueType',
        'year',
        'topic',
      ],
      properties: {
        title: {
          description: 'Title of the research.',
          type: 'string',
        },
        authors: {
          description: 'Authors of the research.',
          type: 'array',
          items: {
            description: 'Author of the research.',
            type: 'string',
          },
        },
        venue: {
          description: 'Publication venue of the research.',
          type: 'string',
        },
        venueType: {
          description: 'Type of the publication venue.',
          type: 'string',
        },
        year: {
          description: 'Year of publication.',
          type: 'number',
        },
        code: {
          description: 'Url of source code.',
          type: 'string',
        },
        demo: {
          description: 'Url of publication demo.',
          type: 'string',
        },
        url: {
          description: 'Url of publication (on publication venue).',
          type: 'string',
        },
        topic: {
          description: 'Research topic.',
          type: 'string',
        },
        subfields: {
          description: 'Subfields under the research topic.',
          type: 'array',
          items: {
            description: 'Subfield under the research topic.',
            type: 'string',
          },
        },
        professorPreferredFormat: {
          description: 'professor preferred format of the publication',
          type: 'string',
        },
      },
    },
  }

  const validator = (input, type) => {
    if (type === 'array') {
      return Array.isArray(input)
    }
    return (typeof input) === type
  }

  // Check each field with above schema. `researchData` must be an array.
  if (!validator(researchData, schema.type)) {
    const tp = schema.type
    throw new Error(`researchData is not type ${tp}.`)
  }

  researchData.forEach((research) => {
    // Each item in `researchData` must be an object.
    if (!validator(research, schema.items.type)) {
      console.error(research)
      const tp = schema.items.type
      throw new Error(`research is not an array of ${tp}.`)
    }

    // Title must be filled.
    if (!validator(research.title, schema.items.properties.title.type)) {
      console.error(research)
      const tp = schema.items.properties.title.type
      throw new Error(`research.title is not type ${tp}.`)
    }

    // Author names must be an array and must be filled.
    if (!validator(research.authors, schema.items.properties.authors.type)) {
      console.error(research)
      const tp = schema.items.properties.authors.type
      throw new Error(`research.authors is not type ${tp}.`)
    }
    research.authors.forEach((author) => {
      if (!validator(author, schema.items.properties.authors.items.type)) {
        console.error(research)
        const tp = schema.items.properties.authors.items.type
        throw new Error(`research.authors is not an array of ${tp}.`)
      }
    })

    // Check if publication venue is filled.
    if (!validator(research.venue, schema.items.properties.venue.type)) {
      console.error(research)
      const tp = schema.items.properties.venue.type
      throw new Error(`research.venue is not type ${tp}.`)
    }

    // Check if venue type is filled.
    if (!validator(
        research.venueType,
        schema.items.properties.venueType.type,
    )) {
      console.error(research)
      const tp = schema.items.properties.venueType.type
      throw new Error(`research.venueType is not type ${tp}.`)
    }

    // Check if publication year is filled within correct range.
    if (research.year) {
      const startYr = 1990
      const endYr = new Date(Date.now()).getFullYear()
      if (!validator(research.year, schema.items.properties.year.type) ||
        research.year < startYr ||
        research.year > endYr + 1) {
        console.error(research)
        const err = `research.year must be in range [${startYr}, ${endYr}].`
        throw new Error(err)
      }
    }

    // Check if publication source code is filled.
    if (research.code) {
      if (!validator(research.code, schema.items.properties.code.type)) {
        console.error(research)
        const tp = schema.items.properties.code.type
        throw new Error(`research.code is not type ${tp}.`)
      }
    }

    // Check if publication demo is filled.
    if (research.demo) {
      if (!validator(research.demo, schema.items.properties.demo.type)) {
        console.error(research)
        const tp = schema.items.properties.demo.type
        throw new Error(`research.demo is not type ${tp}.`)
      }
    }

    // Check if publication url is filled.
    if (research.url) {
      if (!validator(research.url, schema.items.properties.url.type)) {
        console.error(research)
        const tp = schema.items.properties.url.type
        throw new Error(`research.url is not type ${tp}.`)
      }
    }

    // Check if research topic is filled.
    if (!validator(research.topic, schema.items.properties.topic.type)) {
      console.error(research)
      const tp = schema.items.properties.topic.type
      throw new Error(`research.topic is not type ${tp}.`)
    }

    // Check if research subfields is filled. `researchData.subfields` must be
    // an array.
    if (research.subfields) {
      if (!validator(
          research.subfields,
          schema.items.properties.subfields.type,
      )) {
        console.error(research)
        const tp = schema.items.properties.subfields.type
        throw new Error(`research.subfields is not type ${tp}.`)
      }
      research.subfields.forEach((subfield) => {
        if (!validator(
            subfield,
            schema.items.properties.subfields.items.type,
        )) {
          console.error(research)
          const tp = schema.items.properties.subfields.items.type
          throw new Error(`research.subfields is not an array of ${tp}.`)
        }
      })
    }


    // Check if professorPreferredFormat format is filled.
    if (!validator(
        research.professorPreferredFormat,
        schema.items.properties.professorPreferredFormat.type)) {
      console.error(research)
      const tp = schema.items.properties.professorPreferredFormat.type
      throw new Error(`research.professorPreferredFormat is not type ${tp}.`)
    }
  })
}

